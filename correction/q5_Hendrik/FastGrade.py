        fyle = open(file_name, 'w')
        fyle.write('wrongpython = True\n')
        fyle.write("""try: exec('print "system default version of python must be 3 instead of 2"')\nexcept: wrongpython = False\n""")
        fyle.write('if not wrongpython:\n')
